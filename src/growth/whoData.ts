export type Sex = "male" | "female";
export type GrowthMetric = "weight" | "height" | "headCircumference";

export interface LMSRow {
  month: number;
  L: number;
  M: number;
  S: number;
}

export type LMSTable = LMSRow[];

function lms(month: number, L: number, M: number, S: number): LMSRow {
  return { month, L, M, S };
}

export function getLMSTable(sex: Sex, metric: GrowthMetric): LMSTable {
  if (metric === "weight") return sex === "male" ? BOYS_WEIGHT : GIRLS_WEIGHT;
  if (metric === "height") return sex === "male" ? BOYS_HEIGHT : GIRLS_HEIGHT;
  return sex === "male" ? BOYS_HC : GIRLS_HC;
}

