export function zToPercentile(z: number): number {
  const c0 = 2.515517;
  const c1 = 0.802853;
  const c2 = 0.010328;
  const d1 = 1.432788;
  const d2 = 0.189269;
  const d3 = 0.001308;
  const sign = z < 0 ? -1 : 1;
  const w = Math.sqrt(Math.log(1 + (sign * z) ** 2));
  return Math.min(100, Math.max(0, 100 - 100 * Math.exp(
    -((c0 + c1 * w + c2 * w * w) / (1 + d1 * w + d2 * w * w + d3 * w * w * w))
  )));
}

export function valueForZ(L: number, M: number, S: number, z: number): number {
  if (L === 0) return M * Math.exp(S * z);
  const t = 1 + L * S * z;
  if (t <= 0) return M * Math.exp(S * z);
  return M * Math.pow(t, 1 / L);
}

export function zScoreForValue(L: number, M: number, S: number, x: number): number {
  if (x <= 0) return -5;
  if (L === 0) return Math.log(x / M) / S;
  return (Math.pow(x / M, L) - 1) / (L * S);
}

export function percentileForValue(L: number, M: number, S: number, x: number): number {
  return zToPercentile(zScoreForValue(L, M, S, x));
}

